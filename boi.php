            <p>It is observed that it is virtually impossible to grow anything under shadow of big canopy
of trees. Even growth of grass under shade is not possible in parks. 
This phenomenon acquires more importance when we think of fertile land going
Un- utilized under shadow of fruit trees in gardens and wise versa in big fields of wheat and rice where only crop is grown while space available at height can be utilized for growing of fruit trees.
Dense canopy of trees put dense shade on ground under which it is not possible to
grow crops. Accordingly, this space goes wasted. Height of unutilized Space under Tree
canopy is 8-10 feet or may be managed if required so.
Similarly; in grape orchids where grape plants are allowed to spread on stands; shadow
of grape plants does not allow anything to grow on ground. Thus, a vast land is left
vacant. This vacant land is fertile enough to grow any crop and there is ample space as
grapes stands are almost at height of 5-6 feet and height of stands can also be
increased if required so.
In fields; wheat, rice or other cereal crops are cultivated. Cereal crops attain maximum
height of 3-5 feet. Space available upwards is left unused. This large Space can be
utilized to grow fruit / commercial wood trees by using this system. 
Though various efforts have been made to channel sunlight, they mostly pertain to requirement of interiors of residential and/or commercial buildings and do not address specific need of providing sun light evenly as is the requirement for growing crop in a shaded area such as under the canopy of trees or under the stands of grape producing vines. 
There is therefore a need for providing devices that can channel sun light under shadow of trees so that the uncultivated fertile land under trees can be gainfully utilized. Such a device shall provide following advantages and therefore can bring overall prosperity to individual farmers and society at large. More efficient use of fertile land which is already scarce to find in world.
 In parks green grass can be successfully grown under tree shadow. 

In this system; sunlight travels through the sun tube to be diffused upon the crops evenly by low RPM rotating diffusing unit. This makes possible photosynthesis process of plants / crops. Hence any sun loving crop can be cultivated under shadow by using this invention. 


